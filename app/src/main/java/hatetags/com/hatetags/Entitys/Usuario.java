package hatetags.com.hatetags.Entitys;

public class Usuario {

    private Long id;

    private Long idUsuario;

    private String nmUsuario;

    private String tagUsuario;

    private Long nrSeguidores;

    private Long nrAmigos;

    private String nmIdioma;

    private String nmLocalizacao;

    private Long nrTweets;

    private String urlPerfil;

    private int snVerificado;

    private int snTradutor;

    private String nmImagem;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getIdUsuario() {
        return idUsuario;
    }

    public void setIdUsuario(Long idUsuario) {
        this.idUsuario = idUsuario;
    }

    public String getNmUsuario() {
        return nmUsuario;
    }

    public void setNmUsuario(String nmUsuario) {
        this.nmUsuario = nmUsuario;
    }

    public String getTagUsuario() {
        return tagUsuario;
    }

    public void setTagUsuario(String tagUsuario) {
        this.tagUsuario = tagUsuario;
    }

    public Long getNrSeguidores() {
        return nrSeguidores;
    }

    public void setNrSeguidores(Long nrSeguidores) {
        this.nrSeguidores = nrSeguidores;
    }

    public Long getNrAmigos() {
        return nrAmigos;
    }

    public void setNrAmigos(Long nrAmigos) {
        this.nrAmigos = nrAmigos;
    }

    public String getNmIdioma() {
        return nmIdioma;
    }

    public void setNmIdioma(String nmIdioma) {
        this.nmIdioma = nmIdioma;
    }

    public String getNmLocalizacao() {
        return nmLocalizacao;
    }

    public void setNmLocalizacao(String nmLocalizacao) {
        this.nmLocalizacao = nmLocalizacao;
    }

    public Long getNrTweets() {
        return nrTweets;
    }

    public void setNrTweets(Long nrTweets) {
        this.nrTweets = nrTweets;
    }

    public String getUrlPerfil() {
        return urlPerfil;
    }

    public void setUrlPerfil(String urlPerfil) {
        this.urlPerfil = urlPerfil;
    }

    public int getSnVerificado() {
        return snVerificado;
    }

    public void setSnVerificado(int snVerificado) {
        this.snVerificado = snVerificado;
    }

    public int getSnTradutor() {
        return snTradutor;
    }

    public void setSnTradutor(int snTradutor) {
        this.snTradutor = snTradutor;
    }

    public String getNmImagem() {
        return nmImagem;
    }

    public void setNmImagem(String nmImagem) {
        this.nmImagem = nmImagem;
    }
}
