package hatetags.com.hatetags.Entitys;

public class Sentimento {

    private Long idSentimento;

    private Long idTweet;

    private Long idApi;

    private Double rsSentimento;

    public Long getIdSentimento() {
        return idSentimento;
    }

    public void setIdSentimento(Long idSentimento) {
        this.idSentimento = idSentimento;
    }

    public Long getIdTweet() {
        return idTweet;
    }

    public void setIdTweet(Long idTweet) {
        this.idTweet = idTweet;
    }

    public Long getIdApi() {
        return idApi;
    }

    public void setIdApi(Long idApi) {
        this.idApi = idApi;
    }

    public Double getRsSentimento() {
        return rsSentimento;
    }

    public void setRsSentimento(Double rsSentimento) {
        this.rsSentimento = rsSentimento;
    }
}
